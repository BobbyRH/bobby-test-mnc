<h2> Jelaskan perbedaan , mysql_fetch_Array() dan mysql_fetch_assoc()  </h2>

<h3>mysql_fetch_row()</h3>
<p>
menampilkan hasil query mysql keseluruhan detail baris, yang dikembalikan sebagai array dengan indeks angka yang sesuai.
</p>


<h3>mysql_fetch_Array()</h3>
<p>
menampilkan hasil query mysql struktur data dalam bentuk array asosiatif maupun array numerik. 
</p>


<h3>mysql_fetch_assoc()</h3>
<p>
menampilkan hasil query mysql array asosiatif dimana index-nya merepresentasikan nama kolom yang diseleksi. 
</p>


