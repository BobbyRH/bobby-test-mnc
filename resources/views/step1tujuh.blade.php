<h2> Apa yang di maksut dengan constructor, method, class, object, variabel, parameter, function, procedure.
 </h2>

<h3>constructor</h3>
<p>method yang akan dijalankan ketika kita membuat objek dari sebuah class.</p>
<hr/>

<h3>method</h3>
<p>adalah fungsi yang berada pada sebuah class.</p>
<hr/>

<h3>class</h3>
<p> adalah cetak biru atau blueprint dari object, yg bisa dijadikan dasar dan acuan untuk membuat kerangka dasar.</p>
<hr/>

<h3>object</h3>
<p> adalah hasil real atau konkrit dari class.</p>
<hr/>

<h3>variabel</h3>
<p>Variabel dalam PHP merupakan memori untuk menampung dan menyimpan data atau nilai. Sedangkan Variabel Scope di PHP dibagi menjadi 3 antara lain : variabel Global, variabel Local, dan variabel static. Berikut ulasan selengkapnya tentang variabel dan ketiga variabel scope tersebut.</p>
<hr/>

<h3>parameter</h3>
<p> adalah suatu nilai (berupa variabel) yang dikirimkan  ke dalam fungsi, yang kemudian akan ikut diproses di dalam badan fungsi.</p>
<hr/>

<h3>function</h3>
<p> adalah sekumpulan intruksi yang dibungkus dalam sebuah blok, blok program yang dapat mengirim dan mengembalikan nilai hasil setelah di proses</p>
<hr/>

<h3>procedure</h3>
<p>Sebuah perintah yang dapat digunakan untuk membagi beberapa kejadian dalam suatu kumpulan perintah yang lebih kecil dangan berbagai kelengkapan di dalamnya baik itu pengecekan kondisi, fungsi matematika maupun fungsi string. prosedur tidak dapat mengembalikan nilai.</p>
