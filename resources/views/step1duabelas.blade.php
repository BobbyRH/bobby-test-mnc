<h2>Jelaskan perbedaan Cookies dan Session.</h2>
<h3>Session</h3>
metode menyimpan suatu data, untuk membatasi hak akses untuk sebuah halaman tertentu dalam website.

<h3>Cookies</h3>
metode menyimpan suatu data, digunakan untuk memberi batas waktu kepada pengunjung untuk mengakses halaman web. 

