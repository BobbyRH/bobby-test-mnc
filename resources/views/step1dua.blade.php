<h2> Jelaskan perbedaan =, ==, dan === </h2>
<div>
<b>=</b> digunakan untuk mengisi nilai pada variabel.<br/>
<b>==</b> digunakan untuk mendapatkan nilai true jika variabel $a dan $b bernilai sama, tanpa melihat tipe data yang digunkan masing-masing variabel.<br/>
<b>===</b> digunakan untuk mendapatkan nilai true jika variabel $a dan $b bernilai sama dan tipe data yang digunakan masing-masing variabel juga sama.<br/>

