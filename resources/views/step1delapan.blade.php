<h2> Sebutkan dan jelaskan tipe-tipe error dalam PHP </h2>

<h3>Parse Error (Syntax Error)</h3>
<p>error yang diakibatkan kesalahan syntax dalam script sehingga menghentikan proses eksekusi script, biasanya diakibatkan lupa melengkapi script seperti titik koma, tanda kurung yang salah, dan lain sebagainya dan  pada pesan error diberikan informasi line script yang error.
</p>
<hr/>
<h3>Fatal Error</h3>
<p>Error yang diakibatkan dari class yang tidak tersedia tetapi nama class tersebut dipanggil pada script, dan pada pesan error diberikan informasi line script yang error.</p>
<hr/>
<h3>Warning Error</h3>
<p>error yang diakibatkan dari script yang mengambil dan memanggil file yang tidak tersedia pada path yang dituju, dan  pada pesan error diberikan informasi line script yang error.</p>
<hr/>
<h3>Notice Error</h3>
<p>error yang diakibatkan dari script yang mengambil dan memanggil variabel yang belum didefinisikan, dan  pada pesan error diberikan informasi line script yang error.</p>
