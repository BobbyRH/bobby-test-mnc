<h2> Jelaskan perbedaan method=”Post” dan “Get”? </h2>
<div>
<b>POST</b> = adalah method POST yang digunakan pada form, akan tetapi method POST tidak dapat dilihat oleh semua orang melalui address pada browser, dan batas limit dari informasi yang dikirim secara default adalah 8 MB.<br/>
<b>GET</b> = adalah method GET yang digunakan pada form, akan tetapi metode GET dapat dilihat oleh semua orang melalui jendela adress pada browser, dan tidak direkomendasikan untuk data besar yang di kirim dengan method GET lebih dari 2000 karakter.<br/>

