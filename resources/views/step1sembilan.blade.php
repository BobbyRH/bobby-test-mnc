<h2> Apa itu Curl, dan sebutkan langkah-langkah penggunaan Curl di PHP.  </h2>

<p>Curl adalah library yang sering digunakan untuk memindai data dari atau kesuatu server tanpa interaksi dari user.
</p>
<p>Caranya :</p>
<ol>
    <li>tambahkan init curl di awal script</li>
    <li>tentukan target atau url halaman web yang ingin kita load</li>
    <li>tambahkan opsi-opsi untuk CURL</li>
    <li>selanjutnya eksekusi query</li>
    <li>menghentikan proses CURL System</li>
</ol>
