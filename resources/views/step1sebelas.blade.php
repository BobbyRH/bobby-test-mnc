



<h2> Jelaskan perbedaan SQL vs NoSQL dan berikan contoh yang kamu ketahui. </h2>
<h3>SQL</h3>
<p>sebuah bahasa yang dipergunakan untuk mengakses data dalam basis data relasional.</p>
<p>Contohnya : </p>
<ol>
    <li>Mysql</li>
    <li>Ms.Access</li>
    <li>Oracle DB</li>
    <li>PhpMyadmin</li>
    <li>SQL Server</li>
</ol>
<p>untuk salah satu perintahnya adalah <i>select * from table</i></p>
<p>Maka akan tampil data collection didalam tabel yang ditargetkan.</p>


<h3>NoSQL</h3>
<p>sebuah konsep mengenai penyimpanan data non-relasional.</p>
<p>Contohnya : </p>
<ol>
    <li>MonggoDB</li>
    <li>Redis</li>
    <li>Cassandra DB</li>
    <li>Couch DB</li>
</ol>
<p>untuk salah satu perintahnya adalah <i>db.getCollection("datauser").find()</i></p>
<p>Maka akan tampil data berdasarkan collection yang ditargetkan.</p>
